var status = -1;
var selectionLog = [];
function start() {
  action(1, 0, 0);
}
function action(f, E, e) {
  if (status == 0 && f == 0) {
    cm.dispose();
    return;
  }
  status++;
  selectionLog[status] = e;
  var V = -1;
  if (status <= V++) {
    cm.dispose();
  } else if (status === V++) {
    if (cm.isQuestActive(39918) && cm.getNumberFromQuestInfo(39918, "dir") == 0) {
      cm.warp(993151022, 0, true);
    }
    cm.dispose();
  }
}
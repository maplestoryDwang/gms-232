var status = -1;
var selectionLog = [];
function start(g, w, a) {
  if (status == 0 && g == 0) {
    cm.dispose();
    return;
  }
  if (g == 1) {
    status++;
  } else {
    status--;
  }
  selectionLog[status] = a;
  var v = -1;
  if (status <= v++) {
    cm.dispose();
  } else {
    if (status == v++) {
      cm.sendNormalTalk_Bottom("#face4#嘶，脑门现在还火辣辣地疼。\r\n走吧……在右边。", 36, 3004322, false, true);
    } else if (status === v++) {
      cm.forceStartQuest(39721, '');
      cm.dispose();
    }
  }
}

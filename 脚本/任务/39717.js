var status = -1;
var selectionLog = [];
function start(g, w, a) {
  if (status == 0 && g == 0) {
    cm.dispose();
    return;
  }
  if (g == 1) {
    status++;
  } else {
    status--;
  }
  selectionLog[status] = a;
  var v = -1;
  if (status <= v++) {
    cm.dispose();
  } else {
    if (status == v++) {
      cm.sendNormalTalk_Bottom("#face3#唉……那也没有办法了。", 36, 3004322, false, true);
    } else {
      if (status === v++) {
        cm.askYesNo_Bottom("#face3#走吧，右边有老师的踪迹。", 36, 3004322);
      } else if (status === v++) {
        cm.forceStartQuest(39717, '');
        cm.dispose();
      }
    }
  }
}

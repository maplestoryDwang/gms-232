var status = -1;
var selectionLog = [];

function start(d, c, b) {
    if (status == 0 && d == 0) {
        cm.dispose();
        return
    }(d == 1) ? status++ : status--;
    selectionLog[status] = b;
    var a = -1;
    if (status <= a++) {
        cm.dispose()
    } else {
        if (status == a++) {
            cm.sendNormalTalk("狂龙战士。#p3000016#真的离开了吗？我们的攻击队要解散了吗？", 0, 3000017, false, true)
        } else {
            if (status === a++) {
                cm.askAcceptDecline("赶快追踪#p3000016#。他好像跑到东侧去了！#r(接受后将立即移动到相应地图。如果想重新开始任务，请放弃该任务后再重新开始。)#k", 1, 3000017)
            } else {
                if (status === a++) {
                    cm.forceStartQuest(25761, "");
                    cm.dispose();
                    cm.warp(940000180, 0, false)
                }
            }
        }
    }
}


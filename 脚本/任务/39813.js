var status = -1;
var selectionLog = [];
function start(g, w, a) {
  if (status == 0 && g == 0) {
    cm.dispose();
    return;
  }
  if (g == 1) {
    status++;
  } else {
    status--;
  }
  selectionLog[status] = a;
  var v = -1;
  if (status <= v++) {
    cm.dispose();
  } else {
    if (status == v++) {
      cm.sendNormalTalk_Bottom("#face0##h0#，你先回去吧。我还有些事需要调查一下。", 36, 3004431, false, true);
    } else {
      if (status == v++) {
        cm.askYesNoS_Bottom("（要不先去广场看看？）");
      } else if (status === v++) {
        cm.OnStartNavigation(410000500, 1, '', 39813);
        cm.forceStartQuest(39813, '');
        cm.dispose();
      }
    }
  }
}

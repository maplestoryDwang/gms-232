var status = -1;
var selectionLog = [];

function start(d, c, b) {
    if (status == 0 && d == 0) {
        cm.dispose();
        return
    }(d == 1) ? status++ : status--;
    selectionLog[status] = b;
    var a = -1;
    if (status <= a++) {
        cm.dispose()
    } else {
        if (status == a++) {
            if (cm.getMapId() >= 867147000 && cm.getMapId() < 867148000) {
                cm.askYesNo("要回到原来的位置吗？", 4, 9401021)
            } else {
                cm.askYesNo("要立刻移动到 <深渊远征队总部>吗？", 4, 9401021)
            }
        } else {
            if (status === a++) {
                if (cm.getMapId() >= 867147000 && cm.getMapId() < 867148000) {
                    cm.warp(cm.getNumberFromQuestInfo(64759, "rMap"), 0)
                } else {
                    cm.updateInfoQuest(64759, "rMap=" + cm.getMapId());
                    cm.warp(867147001, 0, false)
                }
                cm.dispose()
            }
        }
    }
}


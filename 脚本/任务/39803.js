var status = -1;
var selectionLog = [];
function start(g, w, a) {
  if (status == 0 && g == 0) {
    cm.dispose();
    return;
  }
  if (g == 1) {
    status++;
  } else {
    status--;
  }
  selectionLog[status] = a;
  var v = -1;
  if (status <= v++) {
    cm.dispose();
  } else {
    if (status == v++) {
      cm.sendNormalTalk_Bottom("#face0#我又得再拜托你们进行一次战斗了。", 36, 3004433, false, true);
    } else {
      if (status === v++) {
        cm.askYesNo_Bottom("#face0#西边就交给我，胡克负责北边，切奇负责东边。\r\n#h0#，后方的敌人就拜托你了。", 36, 3004431);
      } else if (status === v++) {
        cm.forceStartQuest(39803, '');
        cm.OnStartNavigation(410000560, 0, "east00", 39803);
        cm.OverlapScreenDetail19(0, 1000, 3000, 1);
        cm.fieldEffect_RemoveOverlapScreenDetail(1000);
        cm.dispose();
      }
    }
  }
}

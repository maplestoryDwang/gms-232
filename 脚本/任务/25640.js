var status = -1;
var selectionLog = [];

function start(d, c, b) {
    if (status == 0 && d == 0) {
        cm.dispose();
        return
    }(d == 1) ? status++ : status--;
    selectionLog[status] = b;
    var a = -1;
    if (status <= a++) {
        cm.dispose()
    } else {
        if (status == a++) {
            cm.sendNext("你是从哪里听说的？不，问题不是这个。你想制作那个东西？\r\n\r\n#b#L0#是的，我有用。#l", 9031004)
        } else {
            if (status == a++) {
                cm.sendNext("好吧，没有我不能做的东西。但是你想用来干什么呢？这一点我想知道。\r\n\r\n#b#L0#我想用那个水晶，保护一个重要的人。#l", 9031004)
            } else {
                if (status == a++) {
                    cm.askAcceptDecline("啊哈~非常好！这个回答听上去真不错。作为答谢，我马上帮你制作。材料带来了吗？", 9031004)
                } else {
                    if (status == a++) {
                        cm.forceStartQuest();
                        cm.dispose()
                    }
                }
            }
        }
    }
}


var status = -1;
var selectionLog = [];

function start(d, c, b) {
    if (status == 0 && d == 0) {
        cm.dispose();
        return
    }(d == 1) ? status++ : status--;
    selectionLog[status] = b;
    var a = -1;
    if (status <= a++) {
        cm.dispose()
    } else {
        if (status == a++) {
            cm.askAcceptDecline("请你去找之前先派到埃德尔斯坦去的情报员。你应该已经见过了吧？她叫#b布吉#k……接受的话，我可以马上把你送到六岔路口的升降场去。请在那里搭乘飞艇，前往埃德尔斯坦。旅行经费请在当地申请报销。", 0, 1101002)
        } else {
            if (status === a++) {
                cm.sendNormalTalk("到达埃德尔斯坦之后，请你到#b埃德尔斯坦散步路1#k去寻找布吉。从村子右边的出口出去，应该就能找到。\r\n\r\n祝你好运。", 1, 1101002, false, true)
            } else {
                if (status === a++) {
                    cm.forceStartQuest(20922, "");
                    cm.dispose();
                    cm.warp(104020130, 0, false)
                }
            }
        }
    }
}


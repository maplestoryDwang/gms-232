var status = -1;
var selectionLog = [];
function start(g, w, a) {
  if (status == 0 && g == 0) {
    cm.dispose();
    return;
  }
  if (g == 1) {
    status++;
  } else {
    status--;
  }
  selectionLog[status] = a;
  var v = -1;
  if (status <= v++) {
    cm.dispose();
  } else {
    if (status == v++) {
      cm.sendNormalTalk_Bottom('监狱在哪边？', 56, 0, false, true);
    } else {
      if (status === v++) {
        cm.askYesNo_Bottom("#face3#是左边。你快赶过去，我随后就到！", 36, 1540451);
      } else if (status === v++) {
        cm.forceStartQuest(39713, '');
        cm.dispose();
      }
    }
  }
}

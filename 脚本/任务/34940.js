var status = -1;
var selectionLog = [];

function start(d, c, b) {
    if (status == 0 && d == 0) {
        cm.dispose();
        return
    }(d == 1) ? status++ : status--;
    selectionLog[status] = b;
    var a = -1;
    if (status <= a++) {
        cm.dispose()
    } else {
        if (status == a++) {
            cm.sendNormalTalk_Bottom("#face5#还没溃退到这里。干得不错。你们的心我已经了解了。", 37, 3001500 + cm.getPlayer().getGender(), 0, 1)
        } else {
            if (status === a++) {
                cm.askAcceptDecline_Bottom("#face8#快走吧。这次该轮到我成为你们的力量了。", 37, 3001500 + cm.getPlayer().getGender())
            } else {
                if (status === a++) {
                    cm.sendNormalTalk_Bottom("#face8#但愿……不要出事。", 37, 3001500 + cm.getPlayer().getGender(), 0, 1)
                } else {
                    if (status === a++) {
                        cm.playerMessage(5, "为了和正在坚守战线的商队成员会合，必须消灭掉魔法士兵。");
                        cm.dispose();
                        cm.openNpc("影魂异人_激战避难处")
                    }
                }
            }
        }
    }
}

function stage0(d, c, b) {
    if (status == 0 && d == 0) {
        cm.dispose();
        return
    }
    status++;
    selectionLog[status] = b;
    var a = -1;
    if (status <= a++) {
        cm.dispose()
    } else {
        if (status == a++) {
            var e = cm.getQuest();
            cm.askYesNo("这个任务的依次对话脚本还没有修复哦。它的脚本位于： #b 脚本/任务/34940.js#k\r\n\r\n如果你有兴趣，欢迎一起来修复！\r\n\r\n那么现在，你要立刻开始这个任务吗？")
        } else {
            if (status == a++) {
                cm.forceStartQuest();
                cm.dispose()
            }
        }
    }
}


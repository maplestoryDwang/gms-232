var status = -1;
var selectionLog = [];

function start(d, c, b) {
    if (status == 0 && d == 0) {
        cm.dispose();
        return
    }(d == 1) ? status++ : status--;
    selectionLog[status] = b;
    var a = -1;
    if (status <= a++) {
        cm.dispose()
    } else {
        if (status == a++) {
            cm.sendNormalTalk("我从别人的记忆中消失的事已经确凿。可是, 如果只是不记得我的话, 那记忆中一定会留下空白的。至少会有‘是谁’之类的疑问。", 16, 0, false, true)
        } else {
            if (status === a++) {
                cm.askAcceptDecline("那么也可以换个角度想, 不是吗? 如果说删除存在不是单纯抹去记忆的话......不管怎样, 我一定要确认一下。那么现在就去吧? \r\n#r(接受任务后将马上移动到相关地图。)", 2, 0)
            } else {
                if (status === a++) {
                    cm.updateInfoQuest(38074, "");
                    cm.forceStartQuest(38074, "");
                    cm.dispose();
                    cm.warp(921110300, 1, false)
                }
            }
        }
    }
}


var status = -1;
var selectionLog = [];
function start(g, w, a) {
  if (status == 0 && g == 0) {
    cm.dispose();
    return;
  }
  if (g == 1) {
    status++;
  } else {
    status--;
  }
  selectionLog[status] = a;
  var v = -1;
  if (status <= v++) {
    cm.dispose();
  } else {
    if (status == v++) {
      cm.sendNormalTalk_Bottom("#face0#走吧，去坟墓那边。", 36, 3004430, false, true);
    } else if (status === v++) {
      cm.forceStartQuest(39815, '');
      cm.OnStartNavigation(410000760, 0, '', 39815);
      cm.dispose();
    }
  }
}

var status = -1;
var selectionLog = [];

function start(d, c, b) {
    if (status == 0 && d == 0) {
        cm.dispose();
        return
    }(d == 1) ? status++ : status--;
    selectionLog[status] = b;
    var a = -1;
    if (status <= a++) {
        cm.dispose()
    } else {
        if (status == a++) {
            cm.sendNext("怎么样？找到机密文件了吗？")
        } else {
            if (status == a++) {
                cm.askAcceptDecline("噢，是吗？真的吗？啊……今天南哈特又要问我关于文件的事情了……如果我说找不到，他肯定会教训我。")
            } else {
                if (status == a++) {
                    cm.forceStartQuest();
                    cm.dispose()
                }
            }
        }
    }
}


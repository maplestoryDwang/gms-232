var status = -1;
var selectionLog = [];

function start(d, c, b) {
    if (status == 0 && d == 0) {
        cm.dispose();
        return
    }(d == 1) ? status++ : status--;
    selectionLog[status] = b;
    var a = -1;
    if (status <= a++) {
        cm.dispose()
    } else {
        if (status == a++) {
            cm.sendNext("喂，你！\r\n\r\n#b#L0#嗯？你是在叫我吗？#l")
        } else {
            if (status == a++) {
                cm.sendNext("没错。你，长得挺漂亮的，愿意加入我们的组织吗？\r\n\r\n#b#L0#没头没脑的让我加入你们组织，你在说什么啊？#l")
            } else {
                if (status == a++) {
                    cm.sendNext("忘了自我介绍了。我是大魔法师弗朗西斯，是伟大的组织黑色之翼的干部。\r\n\r\n#b#L0#黑色之翼？好像在哪听过……#l")
                } else {
                    if (status == a++) {
                        cm.askAcceptDecline("不要让我等太久。")
                    } else {
                        if (status == a++) {
                            cm.forceStartQuest();
                            cm.dispose()
                        }
                    }
                }
            }
        }
    }
}


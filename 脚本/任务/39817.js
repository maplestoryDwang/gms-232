var status = -1;
var selectionLog = [];
function start(g, w, a) {
  if (status == 0 && g == 0) {
    cm.dispose();
    return;
  }
  if (g == 1) {
    status++;
  } else {
    status--;
  }
  selectionLog[status] = a;
  var v = -1;
  if (status <= v++) {
    cm.dispose();
  } else {
    if (status == v++) {
      cm.sendNormalTalk_Bottom("#face1#这里就交给我，你快下去帮忙吧。", 37, 3004435, false, true);
    } else if (status === v++) {
      cm.forceStartQuest(39817, '');
      cm.OnStartNavigation(410000630, 0, "west00", 39817);
      cm.dispose();
    }
  }
}

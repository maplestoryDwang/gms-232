var status = -1;
var selectionLog = [];

function start(d, c, b) {
    if (status == 0 && d == 0) {
        cm.dispose();
        return
    }(d == 1) ? status++ : status--;
    selectionLog[status] = b;
    var a = -1;
    if (status <= a++) {
        cm.dispose()
    } else {
        if (status == a++) {
            cm.askYesNo("隶属于联盟的所有人，我来介绍一下埃德尔斯坦地下矿山发生的事件。在黑色之翼内部好像发生了很大的变化。我需要你的帮助。请马上到#m310010000#来。你想现在移动吗？", 0, 2151003)
        } else {
            if (status === a++) {
                cm.forceStartQuest(1800, "");
                cm.forceCompleteQuest(1800);
                cm.playerMessage(5, "移动到反抗者总部——秘密广场。");
                cm.dispose();
                cm.warp(310010000, 0, false)
            }
        }
    }
}


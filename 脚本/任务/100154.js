var status = -1;
var selectionLog = [];

function start(d, c, b) {
    if (status == 0 && d == 0) {
        cm.dispose();
        return
    }(d == 1) ? status++ : status--;
    selectionLog[status] = b;
    var a = -1;
    if (status <= a++) {
        cm.dispose()
    } else {
        if (status == a++) {
            cm.askMenu("#b#ho##k，你好！有什么事吗？\r\n\r\n#L0# 我想领取酥脆甜饼伤害皮肤！#l\r\n", 4, 1012108)
        } else {
            if (status === a++) {
                cm.sendNormalTalk("0～9必须全部装饰一次以上，才能领取伤害皮肤。", 4, 1012108, false, true)
            } else {
                if (status === a++) {
                    cm.sendNormalTalk("请搜集饼干，然后对伤害皮肤进行装饰！", 4, 1012108, true, false)
                } else {
                    if (status === a++) {
                        cm.dispose()
                    }
                }
            }
        }
    }
}


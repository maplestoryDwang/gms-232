var status = -1;
var selectionLog = [];

function start(d, c, b) {
    if (status == 0 && d == 0) {
        cm.dispose();
        return
    }(d == 1) ? status++ : status--;
    selectionLog[status] = b;
    var a = -1;
    if (status <= a++) {
        cm.dispose()
    } else {
        if (status == a++) {
            cm.sendNormalTalk("你想学宏武团真正的武功吗？", 0, 9330209, false, true)
        } else {
            if (status === a++) {
                cm.sendNormalTalk("要是你能够战胜我所准备的考验，那么我就教你武功吧。", 0, 9330209, true, true)
            } else {
                if (status === a++) {
                    cm.askYesNo("准备好了吗？", 0, 9330209)
                } else {
                    if (status === a++) {
                        cm.sendNormalTalk("那么，现在帮忙进行传送。", 0, 9330209, false, true)
                    } else {
                        if (status === a++) {
                            cm.forceStartQuest(52416, "");
                            cm.dispose();
                            cm.warp(743000400, 0, false)
                        }
                    }
                }
            }
        }
    }
}


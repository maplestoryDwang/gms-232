                cm.askYesNo("想要更详细地了解#r传闻#k吗？\r\n那就亲自去时间神殿调查吧。\r\n#b神殿管理人#k应该很清楚到底发生了什么。\r\n\r\n#b（同意后将向时间神殿移动。）#k", 4, 9010010)
